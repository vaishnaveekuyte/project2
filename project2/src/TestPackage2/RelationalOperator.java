package TestPackage2;

public class RelationalOperator {
	public static void main(String[] args) {
		
	int	a=47,b=57;
	
	System.out.println(a<b);//lessthan
	
	System.out.println(a>b);//greaterthan
	
	System.out.println(a<=b);//lessthan equal to
	
	System.out.println(a>=b);//greaterthan equal to
	
	System.out.println(a==b);//equal to
	
	System.out.println(a!=b);//not equal to
		
		
	}

}
