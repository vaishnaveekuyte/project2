package TestPackage2;

public class IfElseif {
	public static void main(String[] args) {
		
		int age=49;
		if (age >40) {
			System.out.println("the person is 49 year old");
		
	}
		else if(age>=34) {
			System.out.println("the person is 34 year old");
			
		}
		else if(age!=30) {
			System.out.println("the person is 30 year old");
			
		}
		else if(age==49) {
			System.out.println("the person is 49 year old");
		}
		else if(age<=55) {
			System.out.println("the person is 55 year old");
		}
}
}
