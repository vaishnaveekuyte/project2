package TestPackage2;

public class AndOr {
	public static void main(String[] args) {
		
		//and operator(&&)
		System.out.println((40<20) && (30<10));
		
		System.out.println((23<13) && (54>43));
		
		System.out.println((67>45) && (78<67));
		
		System.out.println((55>40) && (76>33));
		
		//or operator (||)
		System.out.println((14>87) || (36>98));
		
		System.out.println((63<53) || (3>24));
		
		System.out.println((7>17) || (98<78));
		
		System.out.println((99<110) || (18<45));
		
		//not operator(!)
		System.out.println((29!=47));
		
		System.out.println(!(67>45));
		
	}

}
